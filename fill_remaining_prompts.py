base_path = "/workspace/промты_ботов"

for category, bots in all_bots.items():
    cat_path = os.path.join(base_path, category)
    template = category_templates.get(category, category_templates["01_Салоны_красоты"])
    
    for bot_folder in bots:
        folder_path = os.path.join(cat_path, bot_folder)
        os.makedirs(folder_path, exist_ok=True)
        
        # Определяем target и goal из названия
        bot_name_clean = bot_folder.replace("Бот_", "").replace("_", " ")
        target = "клиентов бизнеса"
        goal = f"автоматизация процесса: {bot_name_clean.lower()}"
        
        prompt_content = template.format(
            target=target,
            goal=goal,
            bot_name=bot_name_clean
        )
        
        prompt_file = os.path.join(folder_path, "prompt.md")
        with open(prompt_file, "w", encoding="utf-8") as f:
            f.write(prompt_content)
        print(f"Updated: {prompt_file}")

print("\n=== All prompts generated! ===")
