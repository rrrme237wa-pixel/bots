def generate_prompt(bot_data):
    return BASE_PROMPT.format(**bot_data)

def main():
    base_path = Path("/workspace/промты_ботов")
    
    for bot_info in ALL_BOTS_DATA:
        cat_path = base_path / bot_info["category"]
        bot_path = cat_path / bot_info["folder"]
        prompt_file = bot_path / "prompt.md"
        
        if not bot_path.exists():
            print(f"⚠️ Папка не найдена: {bot_info['category']}/{bot_info['folder']}")
            continue
        
        prompt_content = generate_prompt(bot_info)
        
        with open(prompt_file, 'w', encoding='utf-8') as f:
            f.write(prompt_content)
        
        lines = len(prompt_content.split('\n'))
        chars = len(prompt_content)
        print(f"✅ {bot_info['category']}/{bot_info['folder']}: {lines} строк, ~{chars//1000}к символов")

if __name__ == "__main__":
    main()
