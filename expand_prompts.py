for bot in BOTS:
    bot['btns'] = '\n'.join([f"     - {b}" for b in bot['bl']])
    p = Path(f"/workspace/промты_ботов/{bot['cat']}/{bot['f']}/prompt.md")
    if p.parent.exists():
        content = FULL_TEMPLATE.format(
            bn=bot['bn'], cx=bot['cx'], gr=bot['gr'], kb=bot['kb'],
            btns=bot['btns'], hd=bot['hd'], ad=bot['ad'], lb=bot['lb'],
            df=bot['df'], md=bot['md'], ev=bot['ev'], st=bot['st'], sc=bot['sc']
        )
        p.write_text(content, encoding='utf-8')
        print(f"OK {bot['cat']}/{bot['f']}")
    else:
        print(f"NO {bot['cat']}/{bot['f']}")
